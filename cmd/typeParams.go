package cmd

////////////////////////////////////////////////////////////////////////////////////////////////////

// copy & replace
type paramsCR struct {
	orig  string
	dest  string
	files []string
	reps  []rep
}

////////////////////////////////////////////////////////////////////////////////////////////////////

func newCR() paramsCR {
	π := paramsCR{}
	π.files = []string{}
	π.reps = []rep{}
	return π
}

func replaceCR(target string, reps []rep) paramsCR {
	π := paramsCR{}
	π.orig = target
	π.reps = reps
	return π
}

func copyCR(orig, dest string) paramsCR {
	π := newCR()
	π.orig = orig
	π.dest = dest
	return π
}

func cloneCR(params paramsCR) paramsCR {
	Ω := newCR()
	Ω.orig = params.orig
	Ω.dest = params.dest
	Ω.files = params.files
	Ω.reps = params.reps
	return Ω
}

